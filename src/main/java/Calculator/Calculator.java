package Calculator;

public class Calculator {
    int num1;
    int num2;

    public Calculator(){
        super();
    }

    public int Addition(int num1, int num2) {
        return num1 + num2;
    }

    public int Substract(int num1, int num2) {
        return num1 - num2;
    }

    public int Multiply(int num1, int num2) {
        return num1 * num2;
    }

    public int Divide(int num1, int num2) {
        return num1 / num2;
    }
}
